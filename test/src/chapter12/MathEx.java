package chapter12;

public class MathEx {

	public static void main(String[] args) {
		System.out.println("Math.abs(10) = " + Math.abs(10));
		System.out.println("Math.abs(-10) = " + Math.abs(-10));
		System.out.println("Math.abs(3.14) = " + Math.abs(3.14));
		System.out.println("Math.abs(-3.14) = " + Math.abs(-3.14));
		System.out.println();
		System.out.println("Math.ceil(3.3) = " + Math.ceil(3.3));
		System.out.println("Math.ceil(-3.3) = " + Math.ceil(-3.3));
		System.out.println();
		System.out.println("Math.floor(3.3) = " + Math.floor(3.3));
		System.out.println("Math.floor(-3.3) = " + Math.floor(-3.3));
		System.out.println();
		System.out.println("Math.max(4, 5) = " + Math.max(4, 5));
		System.out.println("Math.max(4.1, 5.1) = " + Math.max(4.1, 5.1));
		System.out.println();
		System.out.println("Math.min(4, 5) = " + Math.min(4, 5));
		System.out.println("Math.min(4.1, 5.1) = " + Math.min(4.1, 5.1));
		System.out.println();
		System.out.println("Math.random() = " + Math.random());
		System.out.println();
		System.out.println("Math.rint(2.4) = " + Math.rint(2.4));
		System.out.println("Math.rint(-2.4) = " + Math.rint(-2.4));
		System.out.println();
		System.out.println("Math.round(3.4) = " + Math.round(3.4));
		System.out.println("Math.round(3.5) = " + Math.round(3.5));
	}

}
